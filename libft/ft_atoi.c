#include "libft.h"

long long	ft_atoi(const char *str)
{
	long long	num;
	long long	neg;

	num = 0;
	neg = 1;
	while ((*str > 8 && *str < 14) || *str == 32)
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10 + *str - 48;
		str++;
	}
	return (num * neg);
}
